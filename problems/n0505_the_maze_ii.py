import unittest
from collections import deque
from typing import TypeAlias
import heapq

from unittest_data_provider import data_provider  # type: ignore

Point: TypeAlias = tuple[int, int]


class Solution:
    """
    https://leetcode.com/problems/the-maze-ii
    difficulty: medium
    """

    NEIGHBOURS: list[Point] = [
        (0, -1),  # left
        (0, 1),  # right
        (-1, 0),  # up
        (1, 0),  # down
    ]

    WALL = 1

    def shortestDistance(self, maze: list[list[int]], start: list[int], destination: list[int]) -> bool:
        if start == destination:
            return True

        return self.not_exactly_dijkstra(maze, tuple(start), tuple(destination))  # type: ignore
        # return self.dijkstra(maze, tuple(start), tuple(destination))  # type: ignore
        # return self.bfs(maze, tuple(start), tuple(destination))  # type: ignore

    def go_direction(self, maze: list[list[int]], start: Point, direction: Point) -> tuple[Point, int]:
        m, n = start
        distance = 0
        while 0 <= m < len(maze) and 0 <= n < len(maze[m]) and maze[m][n] != self.WALL:
            m += direction[0]
            n += direction[1]
            distance += 1

        return (m - direction[0], n - direction[1]), distance - 1

    def not_exactly_dijkstra(self, maze: list[list[int]], start: Point, final_destination: Point) -> int:
        """
        Took inspiration from this solution:
        https://leetcode.com/problems/the-maze-ii/solutions/1971783/python-a-and-dijkstra-s-with-explanations-beats-100-00
        """
        def distance_to_goal(p: Point) -> int:
            return abs(p[0] - final_destination[0]) + abs(p[1] - final_destination[1])

        distances: dict[Point, int] = {start: 0}
        queue: list[tuple[int, int, Point]] = []
        heapq.heappush(queue, (distance_to_goal(start), 0, start))

        while queue:
            _, d, s = heapq.heappop(queue)
            if s in distances and distances[s] < d:
                continue
            elif s == final_destination:
                return d

            for direction in self.NEIGHBOURS:
                pos, dist = self.go_direction(maze, s, direction)
                new_distance = distances[s] + dist

                if pos not in distances or new_distance < distances[pos]:
                    distances[pos] = new_distance
                    heapq.heappush(queue, (new_distance + distance_to_goal(pos), new_distance, pos))

        return distances[final_destination] if final_destination in distances else -1

    def dijkstra(self, maze: list[list[int]], start: Point, final_destination: Point) -> int:
        distances: dict[Point, int] = {start: 0}
        queue: list[tuple[int, Point]] = []
        heapq.heappush(queue, (0, start))

        while queue:
            d, s = heapq.heappop(queue)
            if s in distances and distances[s] < d:
                continue
            elif s == final_destination:
                return d

            for direction in self.NEIGHBOURS:
                pos, dist = self.go_direction(maze, s, direction)
                new_distance = distances[s] + dist

                if pos not in distances or new_distance < distances[pos]:
                    distances[pos] = new_distance
                    heapq.heappush(queue, (new_distance, pos))

        return distances[final_destination] if final_destination in distances else -1

    def bfs(self, maze: list[list[int]], start: Point, final_destination: Point) -> int:
        distances: dict[Point, int] = {start: 0}
        queue: deque[Point] = deque([start])
        while queue:
            start = queue.pop()

            for direction in self.NEIGHBOURS:
                pos, dist = self.go_direction(maze, start, direction)
                new_distance = distances[start] + dist
                if pos not in distances or pos in distances and new_distance < distances[pos]:
                    # pos was not visited yet, or it's closer than previously found
                    distances[pos] = new_distance
                    queue.appendleft(pos)

        return distances[final_destination] if final_destination in distances else -1


